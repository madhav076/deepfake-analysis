# Reusable analysis pipeline (Images)
# ──────────────────────────────────────────────────────────────────────
def run_analysis(image, report_key="report", is_generated=False):
    """
    Run the full analysis pipeline on *image* and render results.

    PRIORITY RULE: If an AI watermark / logo / text is detected the
    image is immediately classified as FAKE (90%+ confidence) and
    model inference is skipped.

    If *is_generated* is True the image is automatically classified as
    Fake with high confidence (demo mode).  Otherwise the real model
    is used.
    """
    import time

    # ── Flag for watermark-based override ─────────────────────────
    watermark_override = False
    watermark_reason = ""

    if is_generated:
        # ── Auto-classify generated images as Fake ────────────────
        with st.spinner("🔍 Analyzing image with AI model…"):
            time.sleep(0.8)
        label = "Fake"
        pred_value = round(random.uniform(0.90, 0.99), 4)
    else:
        # ── PRIORITY: Check for AI watermark / logo / text ────────
        with st.spinner("🔎 Scanning for AI watermarks & logos…"):
            ai_found, ai_conf, ai_reason = detect_ai_watermark(image)

        if ai_found:
            label = "Fake"
            pred_value = ai_conf
            watermark_override = True
            watermark_reason = ai_reason
            st.toast("🚩 AI watermark detected — classified as FAKE", icon="🚩")
        else:
            # ── Load model ────────────────────────────────────────
            try:
                model = load_inference_model()
            except Exception as e:
                show_alert(f"Failed to load model: {e}", "error")
                return

            # ── Predict ───────────────────────────────────────────
            try:
                with st.spinner("🔍 Analyzing image with AI model…"):
                    time.sleep(0.5)
                    label, pred_value = predict_image(image, model=model)
            except Exception as e:
                show_alert(f"Prediction failed: {e}", "error")
                return

    # ── Store in history ──────────────────────────────────────────
    if "history" not in st.session_state:
        st.session_state["history"] = []
    st.session_state["history"].append({
        "image": image,
        "label": label,
        "confidence": pred_value * 100,
    })

    # ── Prediction badge + confidence — inside result box ─────────
    badge_class = "badge-real" if label == "Real" else "badge-fake"
    box_class = "real" if label == "Real" else "fake"
    confidence_pct = pred_value * 100
    result_icon = "✅" if label == "Real" else "❌"

    st.markdown(
        f'<div class="result-box {box_class}">'
        f'<div class="result-label">Analysis Result</div>'
        f'<div style="margin:0.6rem 0;">'
        f'<span class="{badge_class}">{result_icon} {label}</span></div>'
        f'</div>',
        unsafe_allow_html=True,
    )

    # ── Confidence metric + progress bar ──────────────────────────
    st.markdown(
        f'<div class="metric-card">'
        f'<div class="value">{confidence_pct:.1f}%</div>'
        f'<div class="label">Fake Probability</div></div>',
        unsafe_allow_html=True,
    )
    st.progress(min(pred_value, 1.0))

    st.markdown("")  # spacing

    # ── Structured Analysis Report ─────────────────────────────────
    st.markdown("---")
    st.markdown('<div class="section-title">📊 Analysis Report</div>', unsafe_allow_html=True)

    _ts_str = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    _fake_pct = pred_value * 100
    _real_pct = 100 - _fake_pct
    _method = "AI Watermark Detection" if watermark_override else "AI Generated (Demo)" if is_generated else "Deep Learning CNN"
    _conf_lvl = "Low" if confidence_pct < 50 else "Medium" if confidence_pct < 75 else "High"
    _res_color = "#22C55E" if label == "Real" else "#FF4D4D"

    st.markdown(
        f'<div class="report-grid">'
        f'<div class="report-item">'
        f'<div class="ri-label">🛡️ Detection Result</div>'
        f'<div class="ri-value" style="color:{_res_color}">{result_icon} {label.upper()}</div>'
        f'</div>'
        f'<div class="report-item">'
        f'<div class="ri-label">📊 Confidence Score</div>'
        f'<div class="ri-value">{confidence_pct:.1f}%</div>'
        f'<div class="ri-sub">{_conf_lvl} confidence</div>'
        f'</div>'
        f'<div class="report-item">'
        f'<div class="ri-label">🕐 Analysis Time</div>'
        f'<div class="ri-value" style="font-size:.95rem">{_ts_str}</div>'
        f'</div>'
        f'<div class="report-item">'
        f'<div class="ri-label">🔍 Analysis Method</div>'
        f'<div class="ri-value" style="font-size:.95rem">{_method}</div>'
        f'</div>'
        f'</div>',
        unsafe_allow_html=True,
    )

    # ── Charts: Confidence Gauge + Detection Donut ─────────────────
    _cg_col, _cd_col = st.columns(2)
    with _cg_col:
        st.markdown(
            '<div class="chart-container">'
            '<div class="chart-title">🎯 Confidence Gauge</div>',
            unsafe_allow_html=True,
        )
        _is_real_gauge = _real_pct >= _fake_pct
        render_confidence_gauge(
            _real_pct if _is_real_gauge else _fake_pct,
            "Real" if _is_real_gauge else "Fake",
        )
        st.markdown('</div>', unsafe_allow_html=True)

    with _cd_col:
        st.markdown(
            '<div class="chart-container">'
            '<div class="chart-title">📊 Detection Breakdown</div>',
            unsafe_allow_html=True,
        )
        render_detection_donut(_real_pct, _fake_pct, chart_id=f"donut_{report_key}")
        st.markdown('</div>', unsafe_allow_html=True)

    # ── Face detection & heatmap (side by side) ───────────────────
    col_face, col_heat = st.columns(2)

    with col_face:
        st.markdown("#### 🟩 Face Detection")
        face_img, face_found = detect_face_box(image)
        if not face_found:
            show_alert("No face detected in this image.", "warning")
        st.image(face_img, caption="Detected Face", use_container_width=True)

    with col_heat:
        st.markdown("#### 🔥 Heatmap")
        heatmap = create_heatmap(image)
        st.image(heatmap, caption="Heatmap Overlay", use_container_width=True)

    # ── Explainability ──────────────────────────────────────────────
    if watermark_override:
        explain_ai_watermark(watermark_reason)
    elif is_generated:
        explain_generated()
    else:
        explain_prediction(label, pred_value)

    # ── Downloadable PDF report (PDF ONLY — no .txt) ─────────────
    try:
        pdf_bytes = generate_pdf(
            result=label,
            confidence=confidence_pct,
            pil_image=image,
            watermark_reason=watermark_reason,
        )
        st.download_button(
            label="📄 Download PDF Report",
            data=pdf_bytes,
            file_name="media_verification_report.pdf",
            mime="application/pdf",
            key=f"{report_key}_pdf",
        )
    except Exception as e:
        show_alert(f"PDF generation failed: {e}", "warning")


