def extract_frames(video_path: str, max_frames: int = 12) -> list[tuple[Image.Image, float]]:
    """
    Extract evenly-spaced frames from a video file.

    Returns a list of (PIL Image, timestamp_seconds) tuples.
    """
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        raise ValueError("Could not open video file.")

    fps = cap.get(cv2.CAP_PROP_FPS) or 30.0
    total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    duration = total_frames / fps

    # Choose frame indices evenly across the video
    if total_frames <= max_frames:
        indices = list(range(total_frames))
    else:
        indices = [int(i * total_frames / max_frames) for i in range(max_frames)]

    frames: list[tuple[Image.Image, float]] = []
    for idx in indices:
        cap.set(cv2.CAP_PROP_POS_FRAMES, idx)
        ret, frame = cap.read()
        if not ret:
            continue
        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        pil_img = Image.fromarray(rgb)
        timestamp = idx / fps
        frames.append((pil_img, timestamp))

    cap.release()
    return frames


def check_frame_consistency(frames: list[Image.Image]) -> tuple[float, list[str]]:
    """
    Analyse frame-to-frame visual consistency.

    Returns (inconsistency_score 0-1, list of finding strings).
    Higher score = more inconsistencies = more likely fake.
    """
    if len(frames) < 2:
        return 0.0, ["Not enough frames for consistency analysis"]

    findings: list[str] = []
    scores: list[float] = []

    for i in range(len(frames) - 1):
        a = np.array(frames[i].resize((224, 224))).astype("float32")
        b = np.array(frames[i + 1].resize((224, 224))).astype("float32")

        # 1. Structural difference (MSE)
        mse = np.mean((a - b) ** 2)
        scores.append(min(mse / 5000.0, 1.0))  # normalise

        # 2. Histogram difference (lighting shifts)
        hist_a = cv2.calcHist([a.astype("uint8")], [0], None, [64], [0, 256])
        hist_b = cv2.calcHist([b.astype("uint8")], [0], None, [64], [0, 256])
        hist_diff = cv2.compareHist(
            hist_a.astype("float32"),
            hist_b.astype("float32"),
            cv2.HISTCMP_BHATTACHARYYA,
        )
        if hist_diff > 0.35:
            findings.append(
                f"💡 Sudden lighting change between frame {i+1} → {i+2} "
                f"(Bhattacharyya distance: {hist_diff:.2f})"
            )

        # 3. Edge coherence (Canny)
        edge_a = cv2.Canny(a.astype("uint8"), 100, 200)
        edge_b = cv2.Canny(b.astype("uint8"), 100, 200)
        edge_diff = np.mean(np.abs(edge_a.astype("float32") - edge_b.astype("float32")))
        if edge_diff > 40:
            findings.append(
                f"🌀 Edge structure anomaly between frame {i+1} → {i+2} "
                f"(diff: {edge_diff:.1f})"
            )

    avg_score = float(np.mean(scores)) if scores else 0.0

    if avg_score < 0.1:
        findings.insert(0, "✅ Frames are temporally consistent — smooth transitions")
    elif avg_score > 0.4:
        findings.insert(0, "⚠️ High frame-to-frame variance — possible splicing or AI generation")

    return avg_score, findings


