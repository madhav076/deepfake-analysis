def detect_face_box(image):
    """Draw green bounding boxes around detected faces."""
    img = np.array(image)
    gray = cv2.cvtColor(img, cv2.COLOR_RGB2GRAY)
    face_cascade = cv2.CascadeClassifier(
        cv2.data.haarcascades + "haarcascade_frontalface_default.xml"
    )
    faces = face_cascade.detectMultiScale(gray, 1.3, 5)
    face_found = len(faces) > 0
    for (x, y, w, h) in faces:
        cv2.rectangle(img, (x, y), (x + w, y + h), (0, 255, 0), 3)
    return img, face_found


def create_heatmap(image):
    """Generate a JET colour-mapped heatmap overlay."""
    img = np.array(image)
    img = cv2.resize(img, (224, 224))
    heatmap = cv2.applyColorMap(img, cv2.COLORMAP_JET)
    heatmap = cv2.addWeighted(img, 0.6, heatmap, 0.4, 0)
    return heatmap


